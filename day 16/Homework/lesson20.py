# ცვლადების შექმნა შედარების ოპერაციების შედეგებით
a = 5 > 3   # True: 5 მეტია 3-ზე
b = 10 < 7  # False: 10 ნაკლები არ არის 7-ზე
c = 4 == 4  # True: 4 ტოლია 4-ის
d = 6 != 6  # False: 6 უდრის 6-ს
e = 9 >= 9  # True: 9 მეტია ან ტოლია 9-ის


print(a) # True
print(b) # False
print(c) # True
print(d) # False
print(e) # True

